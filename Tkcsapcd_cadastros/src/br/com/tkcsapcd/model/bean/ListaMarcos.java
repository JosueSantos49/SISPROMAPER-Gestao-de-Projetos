package br.com.tkcsapcd.model.bean;

public class ListaMarcos {

	private Integer codigo;
	private String listamarcos;
	private String projeto;
	private Integer codigoprojeto;
	private String versao;
	private String data;
	private String autor;
	private String notarevisao;
	private String aprovacao;
	private String objetivo;
	private String fases;
	
	public Integer getCodigo() {
		return codigo;
	}
	public void setCodigo(Integer codigo) {
		this.codigo = codigo;
	}
	public String getListamarcos() {
		return listamarcos;
	}
	public void setListamarcos(String listamarcos) {
		this.listamarcos = listamarcos;
	}
	public String getProjeto() {
		return projeto;
	}
	public void setProjeto(String projeto) {
		this.projeto = projeto;
	}
	public Integer getCodigoprojeto() {
		return codigoprojeto;
	}
	public void setCodigoprojeto(Integer codigoprojeto) {
		this.codigoprojeto = codigoprojeto;
	}
	public String getVersao() {
		return versao;
	}
	public void setVersao(String versao) {
		this.versao = versao;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getAutor() {
		return autor;
	}
	public void setAutor(String autor) {
		this.autor = autor;
	}
	public String getNotarevisao() {
		return notarevisao;
	}
	public void setNotarevisao(String notarevisao) {
		this.notarevisao = notarevisao;
	}
	public String getAprovacao() {
		return aprovacao;
	}
	public void setAprovacao(String aprovacao) {
		this.aprovacao = aprovacao;
	}
	public String getObjetivo() {
		return objetivo;
	}
	public void setObjetivo(String objetivo) {
		this.objetivo = objetivo;
	}
	public String getFases() {
		return fases;
	}
	public void setFases(String fases) {
		this.fases = fases;
	}
	
	
}
