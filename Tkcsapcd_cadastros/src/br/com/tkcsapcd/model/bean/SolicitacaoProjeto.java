package br.com.tkcsapcd.model.bean;

public class SolicitacaoProjeto {

	private Integer codigo;
	private String solicitacaoprojeto;
	private String projeto;
	private Integer codigoprojeto;
	private String versao;
	private String data;
	private String autor;
	private String notarevisao;
	private String aprovacao;
	private String objetivo;
	private String atual;
	private String descricao;
	
	public Integer getCodigo() {
		return codigo;
	}
	public void setCodigo(Integer codigo) {
		this.codigo = codigo;
	}
	
	public String getSolicitacaoprojeto() {
		return solicitacaoprojeto;
	}
	public void setSolicitacaoprojeto(String solicitacaoprojeto) {
		this.solicitacaoprojeto = solicitacaoprojeto;
	}
	public String getProjeto() {
		return projeto;
	}
	public void setProjeto(String projeto) {
		this.projeto = projeto;
	}
	public Integer getCodigoprojeto() {
		return codigoprojeto;
	}
	public void setCodigoprojeto(Integer codigoprojeto) {
		this.codigoprojeto = codigoprojeto;
	}
	public String getVersao() {
		return versao;
	}
	public void setVersao(String versao) {
		this.versao = versao;
	}
	public String getData() {
		return data;
	}
	public void setData(String data) {
		this.data = data;
	}
	public String getAutor() {
		return autor;
	}
	public void setAutor(String autor) {
		this.autor = autor;
	}
	public String getNotarevisao() {
		return notarevisao;
	}
	public void setNotarevisao(String notarevisao) {
		this.notarevisao = notarevisao;
	}
	public String getAprovacao() {
		return aprovacao;
	}
	public void setAprovacao(String aprovacao) {
		this.aprovacao = aprovacao;
	}
	public String getObjetivo() {
		return objetivo;
	}
	public void setObjetivo(String objetivo) {
		this.objetivo = objetivo;
	}
	public String getAtual() {
		return atual;
	}
	public void setAtual(String atual) {
		this.atual = atual;
	}
	public String getDescricao() {
		return descricao;
	}
	public void setDescricao(String descricao) {
		this.descricao = descricao;
	}
	
	
}
